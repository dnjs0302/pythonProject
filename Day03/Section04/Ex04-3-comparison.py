'''
관계 연산자
     ex) >, >=, <, <=, ==, !=
     2개 향을 비교하여 그 결과를 논리(bool) 자료형으로 변환
'''
a = 15
print('{} > 10 : {}'.format(a, a > 10))
print('{} < 10 : {}'.format(a, a < 10))
print('{} >= 10 : {}'.format(a, a >= 10))
print('{} <= 10 : {}'.format(a, a <= 10))
print('{} == 10 : {}'.format(a, a == 10)) #'='은 대입연산자이므로 부등호는 두개를 씀
print('{} != 10 : {}'.format(a, a != 10)) # '!=' 은 같지않다는 뜻
